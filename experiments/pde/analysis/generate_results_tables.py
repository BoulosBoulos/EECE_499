"""Generate LaTeX results tables from evaluation CSVs.

Reads eval CSVs from the ablation output, computes mean +/- std across seeds,
runs statistical tests (Welch's t-test), and outputs LaTeX table code.

Usage:
    python experiments/pde/analysis/generate_results_tables.py \
        --eval_dir results/ablation/tier1 \
        --out results/tables/
"""

import argparse
import os
import glob
import numpy as np


def compute_significance(values_method, values_baseline, alpha=0.05):
    """Compute Welch's t-test p-value and significance marker."""
    try:
        from scipy import stats
    except ImportError:
        return 1.0, ""
    if len(values_method) < 2 or len(values_baseline) < 2:
        return 1.0, ""
    _, p_val = stats.ttest_ind(values_method, values_baseline, equal_var=False)
    if p_val < 0.001:
        return p_val, "***"
    elif p_val < 0.01:
        return p_val, "**"
    elif p_val < alpha:
        return p_val, "*"
    return p_val, ""


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--eval_dir", required=True)
    parser.add_argument("--out", default="results/tables")
    args = parser.parse_args()

    try:
        import pandas as pd
    except ImportError:
        print("pandas required: pip install pandas")
        return

    os.makedirs(args.out, exist_ok=True)

    # Find all eval CSVs
    pattern = os.path.join(args.eval_dir, "**", "eval_*.csv")
    csv_files = glob.glob(pattern, recursive=True)
    if not csv_files:
        pattern = os.path.join(args.eval_dir, "ablation_results.csv")
        csv_files = glob.glob(pattern)

    if not csv_files:
        print(f"No eval CSVs found in {args.eval_dir}")
        return

    # Load and concatenate
    dfs = []
    for f in csv_files:
        try:
            dfs.append(pd.read_csv(f))
        except Exception:
            continue

    if not dfs:
        print("No valid CSVs loaded")
        return

    df = pd.concat(dfs, ignore_index=True)
    print(f"Loaded {len(df)} rows from {len(dfs)} files")

    # Generate main table
    methods = ["hjb_aux", "soft_hjb_aux", "eikonal_aux", "cbf_aux", "drppo"]
    metrics = ["mean_return", "collision_rate", "success_rate", "mean_ttc"]
    metric_labels = {"mean_return": "Return", "collision_rate": "Coll. Rate",
                     "success_rate": "Succ. Rate", "mean_ttc": "Mean TTC"}

    out_path = os.path.join(args.out, "main_results.tex")
    with open(out_path, "w") as f:
        f.write("% Auto-generated by generate_results_tables.py\n")
        f.write("\\begin{table*}[ht]\n\\centering\n")
        f.write("\\caption{Main comparison across methods.}\n")
        f.write("\\label{tab:main_results}\n")
        f.write("\\begin{tabular}{l" + "c" * len(methods) + "}\n\\toprule\n")
        f.write("Metric & " + " & ".join(methods) + " \\\\\n\\midrule\n")

        for metric in metrics:
            row = metric_labels.get(metric, metric)
            for method in methods:
                vals = df[df.get("variant", df.get("method", "")) == method][metric].dropna().values \
                    if "variant" in df.columns else np.array([])
                if len(vals) > 0:
                    row += f" & ${np.mean(vals):.2f} \\pm {np.std(vals):.2f}$"
                else:
                    row += " & --"
            row += " \\\\\n"
            f.write(row)

        f.write("\\bottomrule\n\\end{tabular}\n\\end{table*}\n")

    print(f"Saved {out_path}")


if __name__ == "__main__":
    main()
